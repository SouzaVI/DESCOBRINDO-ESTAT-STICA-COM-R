#-------------------------------------------------------------------------------
#                    TESTE t para amostras independentes
#-------------------------------------------------------------------------------
controle     <- c(21, 28, 24, 23, 23, 19, 28, 20, 22, 20, 26, 26)
experimental <- c(26, 27, 23, 25, 25, 29, 30, 31, 36, 23, 32, 22)

par(mfrow=c(2,2))

boxplot(controle,
        col = "#FF8C00",
        border = "#4F4F4F",
        horizontal = F,
        notch = F,
        xlab= "controle"
)

boxplot(experimental,
        col = "blue",
        border = "#4F4F4F",
        horizontal = F,
        notch = F,
        xlab= "experimental"
)

t.test(controle, experimental)

#-------------------------------------------------------------------------------
#                         TESTE t Pareado (para o mesmo grupo)
#-------------------------------------------------------------------------------

t.test(controle, experimental, paired = TRUE)

